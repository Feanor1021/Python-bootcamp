string1="hello"
string2="world"

print(string1+string2) #Concatenation - Adds values on either side of the operator. values are must be strings
print(string1*2) #Repetition - Creates new strings, concatenating multiple copies of the same string
print('h' in string1) #Membership - Returns true if a character exists in the given string
print("5" not in string1) #Membership - Returns true if a character does not exist in the given string
print(r"\n\n"+string1) #Raw String - Suppresses actual meaning of Escape characters.